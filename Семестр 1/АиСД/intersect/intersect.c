#include <stdio.h>
#include <inttypes.h>

typedef uint32_t u32;

u32 read() {
    size_t a_size;
    scanf("%zu", &a_size);
    u32 ans = 0;
    for (size_t i = 0; i < a_size; ++i) {
        uint8_t num;
        scanf("%"SCNu8, &num);
        ans |= UINT32_C(1) << num;
    }
    return ans;
}

signed main() {
    u32 a = read();
    u32 b = read();
    a &= b;
    uint32_t mask = 1;
    for (uint8_t i = 0; i <= UINT8_C(31); ++i) {
        if (mask & a) {
            printf("%"PRIu8" ", i);
        }
        mask *= 2;
    }
    return 0;
}
