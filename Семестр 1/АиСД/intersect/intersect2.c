#include <stdio.h>

unsigned int cin() {
    int n, x;
    unsigned int a = 0;

    scanf("%d", &n);
    for (int i = 0; i < n; i++){
        scanf("%d", &x);
        a += (1 << x);
    }

    return a;
}

int main() {
    unsigned int a = cin(), b = cin();

    unsigned int ans = a & b;
    for (int i = 0; ans > 0; ++i, ans /= 2) {
        if (ans % 2 == 1) {
            printf("%d ", i);
        }
    }

    return 0;
}
